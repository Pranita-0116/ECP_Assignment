//write a programme to accept two numbers and 
//a.find its sum
//b.find its diff
//c.find its prod 
#include<stdio.h>
    
       int main()
{
int n1,n2;
printf("Enter two numbers:");
scanf("%d%d",&n1,&n2);
printf("sum of two numbers:%d/n",n1+n2);
printf("diff of two numbers:%d/n",n1-n2);
printf("prod of two numbers:%d/n",n1*n2);
return 0;
}

